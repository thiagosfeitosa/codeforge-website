type I18nContextValue = {
  language: LanguageCode;
  setLanguage: (language: LanguageCode) => void;
  t: (key: string) => string;
};

const I18nContext = createContext<I18nContextValue | null>(null);

function getPreferredLanguage(): LanguageCode {
  if (typeof window === "undefined") {
    return "en-US";
  }

  const storedLanguage = window.localStorage.getItem("codeforge-language");
  if (storedLanguage === "en-US" || storedLanguage === "pt-BR" || storedLanguage === "es-ES") {
    return storedLanguage;
  }

  return "en-US";
}

function applyLanguage(language: LanguageCode) {
  document.documentElement.lang = language;
  document.documentElement.dataset.language = language;
  window.localStorage.setItem("codeforge-language", language);
}

export function I18nProvider({ children }: { children: ReactNode }) {
  const [language, setLanguageState] = useState<LanguageCode>(() => getPreferredLanguage());

  useEffect(() => {
    applyLanguage(language);
  }, [language]);

  function setLanguage(nextLanguage: LanguageCode) {
    setLanguageState(nextLanguage);
    applyLanguage(nextLanguage);
  }

  function t(key: string) {
    return dictionaries[language][key] ?? dictionaries["en-US"][key] ?? key;
  }

  return (
    <I18nContext.Provider value={{ language, setLanguage, t }}>
      {children}
    </I18nContext.Provider>
  );
}

export function useI18n() {
  const context = useContext(I18nContext);
  if (!context) {
    throw new Error("useI18n must be used within I18nProvider");
  }

  return context;
}

export function T({ k }: { k: string }) {
  const { t } = useI18n();
  return <>{t(k)}</>;
}
